package exam01.sub01;

public class EX05 {
    public static void main(String[] args) {
        long num = 1000_000_000_000L; // int -> long 자료형 long에 값을 쓸때는 맨뒤에 L을 표기한다

        byte num2 = 100; // int  ->  byte

        double num3 = 10.123;
        float num4 = 10.123F; // double -> float 자료형 float에 값을 쓸때는 맨뒤에 F를 표기한다
    }
}
