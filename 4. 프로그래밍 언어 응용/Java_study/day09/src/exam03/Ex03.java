package exam03;

import static java.lang.Math.*;

public class Ex03 {
    public static void main(String[] args) {
        System.out.println("원주율 : " + PI);
        System.out.println("절대값 : " + abs(-10));
        System.out.println("올림 : " + ceil(3.1));
        System.out.println("버림 : " + floor(3.9));
        System.out.println("반올림 : " + round(3.5));
        System.out.println("2의 3승 : " + pow(2,3));
        System.out.println("랜덤 : " + random());
    }
}
